#include <unistd.h>

void	print_bits(char octet)
{
	char  b;
	b = 7;
	while  (b >= 0)
	{
		if (octet & (1U << b))
			write(1, "1", 1);
		else
			write(1, "0", 1);
		b--;
	}
}

int		main()
{
	print_bits(127);
}
